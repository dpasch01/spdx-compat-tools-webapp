    <h1>Contribute</h1>
    
    <div>
        <p>For those of you who want to contribute in the coding, you are all welcome. The source code is available on <a href="https://github.com/dpasch01/spdx-compat-tools">GitHub</a>. The software and the structure of the graph is in Java, whereas it is executed through PHP.
        </p>
    </div>
    
    <div>
        <p>The code related to the <a href="http://spdx.org/">Software Package Data Exchange®</a> (SPDX®) was imported by SPDX's official Work-group Tools, and specifically the <a href="http://spdx.org/tools/spdx/spdx-viewer"> SPDX Viewer</a>. According to SPDX, the SPDX Viewer is a command line driven Java application that formats a valid SPDX RDF document into a text file for easier viewing. The SPDX Viewer application validates the SPDX document during formatting and provides Warning messages when parsing issues are encountered. An attempt will be made to create the text file even if validation errors are encountered.
        </p>
    </div>

    <div>
        <p>
            For the license graph implementation in Java, there has been a usage of several libraries but mainly the <a href="http://jgrapht.org/">JGrapht </a>Java Library which is a free Java graph library that provides mathematical graph-theory objects and algorithms. JGraphT supports various types of graphs.
    There has also been a usage of JGraph library which is focused on rendering and GUI-based editing rather than data structures and algorithms. The visualization and the user's editing menu, was implemented in Javascript and specificaly based on the dynamic, browser based visualization library,<a href="http://visjs.org/"> Vis.js</a>. </p>
